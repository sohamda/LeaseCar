
package com.car.api.style;


public class MPG {

    private String highway;
    private String city;

    /**
     * 
     * @return
     *     The highway
     */
    public String getHighway() {
        return highway;
    }

    /**
     * 
     * @param highway
     *     The highway
     */
    public void setHighway(String highway) {
        this.highway = highway;
    }

    /**
     * 
     * @return
     *     The city
     */
    public String getCity() {
        return city;
    }

    /**
     * 
     * @param city
     *     The city
     */
    public void setCity(String city) {
        this.city = city;
    }
}


package com.car.api.style;


public class Valve {

    private String timing;
    private String gear;

    /**
     * 
     * @return
     *     The timing
     */
    public String getTiming() {
        return timing;
    }

    /**
     * 
     * @param timing
     *     The timing
     */
    public void setTiming(String timing) {
        this.timing = timing;
    }

    /**
     * 
     * @return
     *     The gear
     */
    public String getGear() {
        return gear;
    }

    /**
     * 
     * @param gear
     *     The gear
     */
    public void setGear(String gear) {
        this.gear = gear;
    }
}
